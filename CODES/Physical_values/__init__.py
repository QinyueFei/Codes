## Codes in this module are developed to extract the physical conditions from observations.

## surface_density.py: derive the surface density of gas through moment 0 maps with estimation of a_CO, line ratio and other parameters.

## pycprops: Codes that are used to identify giant molecular clouds (GMC) in disk galaxies, a popular paper that use: Sun+2018. See the detailed algorithm in Rosolowsky & Leroy+2008.

## clouds.py: finds clouds eith cprops algorithm, the identification of clouds are listed in clouds document
## show_clouds.py: overplot the clouds position and shape on moment 0 maps
## clouds_properties.py: obtain the properties of each clouds

